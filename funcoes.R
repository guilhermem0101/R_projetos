# which(): entrada, uma condição lógica sobre um vertor. Saida, os indices dos valores que satiszazem a condição
x <- c(1, 5, 3, 7, 2)
which(x > 3)

# repetir elementos de um vetor x t vezes, sendo t o vetor com o numero de repetições para cada elemento em x.
rep(x, t)

